/*****
描述
    给出一个正整数a，要求分解成若干个正整数的乘积，即a = a1 * a2 * a3 * ... * an，并且
    1 < a1 <= a2 <= a3 <= ... <= an，问这样的分解的种数有多少。注意到a = a也是一种分解。
关于输入
    第1行是测试数据的组数n，后面跟着n行输入。每组测试数据占1行，包括一个正整数a (1 < a < 32768)
关于输出
    n行，每行输出对应一个输入。输出应是一个正整数，指明满足要求的分解的种数
例子输入
    2
    2
    20
例子输出
    1
    4
*****/

#include <iostream>
using namespace std;

int func(int n, int start)
{
	int res = 0;
	for (int i = start; i * i <= n; i++)
		if (n % i == 0)
			res += func(n / i, i);
	return res + 1; // 这个数本身也是一种分解方法
}

int main()
{
	int n_case, n;
	cin >> n_case;
	while (n_case--) {
		cin >> n;
		cout << func(n, 2) << endl;
	}
	return 0;
}
